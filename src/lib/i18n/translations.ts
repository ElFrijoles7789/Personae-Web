export function detectLocale(browserLang: string | undefined): Locale {
  if (!browserLang) return 'es';
  const lang = browserLang.toLowerCase().split(/[-_]/)[0];
  if (LOCALES.includes(lang as Locale)) return lang as Locale;
  return 'es';
}
